using System;
using System.Collections.Generic;
using System.Text;

namespace wi19b065
{
    class Calculator
    {
        public Calculator()
        {
        }
        internal int addiere(int p0, int p1)
        {
            return p0 + p1;
        }
        internal int subtrahiere(int p0, int p1)
        {
            return p0 - p1;
        }

        internal int multipliziere(int p0, int p1)
        {
            return p0 * p1;
        }

        internal int dividiere(int p0, int p1)
        {
            return p0 / p1;
        }
    }
}